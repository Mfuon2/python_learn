temperature = 30

# > greater than
if temperature > 30:
    print('its a hot day')

# < less than
if temperature < 30:
    print('its a cold day')

# >=  or <= less or equal
if temperature >= 30:
    print('greater or equal to')

# == equals
if temperature == 30:
    print('equals to')

